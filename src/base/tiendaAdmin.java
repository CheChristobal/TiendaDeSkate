    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed

        try {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO productos (id_codigo, nombre, precio, stock, imagen, ruta)VALUES (?,?,?,?,?,?)");
            FileInputStream archivofoto;
            pst.setString(1, txtCodigo.getText());
            pst.setString(2, txtNombre.getText());
            pst.setString(3, txtPrecio.getText());
            pst.setString(4, txtStock.getText());
            archivofoto = new FileInputStream(textoImagen.getText());
            pst.setBinaryStream(5, archivofoto);
            pst.setString(6, textoImagen.getText());
            
            
            pst.executeUpdate();
            //mostrarDatos();
            JOptionPane.showMessageDialog(null, "Ingresado con exito");

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

