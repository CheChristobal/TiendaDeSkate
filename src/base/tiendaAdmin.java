    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int seleccion = tablaProductos.getSelectedRow();
        
        
        String valor= tablaProductos.getValueAt(seleccion, 0).toString();
        if(seleccion>0){
            try {
                PreparedStatement pst = cn.prepareStatement("DELETE FROM productos WHERE id_codigo='"+valor+"'");
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato eliminado");
                mostraDatos();
            } catch (SQLException ex) {
                Logger.getLogger(agregarProductos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

